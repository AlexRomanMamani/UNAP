public class Stock {
    // ATRIBUTOS
    int conteo;
    String momento;

    // CONSTRUCTOR

    public Stock(int conteo) {
        this.conteo = conteo;
    }

    // GETTERS Y SETTERS

    public int getConteo() {
        return this.conteo;
    }

    public void setConteo(int conteo) {
        this.conteo = conteo;
    }

    public String getMomento() {
        return this.momento;
    }

    public void setMomento(String momento) {
        this.momento = momento;
    }

    // METODO

}
