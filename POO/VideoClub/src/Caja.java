public class Caja {
    // ATRIBUTOS
    int valorEntrada, valorSalida;

    // CONSTRUCTOR
    public Caja() {
    }

    // GETTERS Y SETTERS
    public int getValorEntrada() {
        return this.valorEntrada;
    }

    public void setValorEntrada(int valorEntrada) {
        this.valorEntrada = valorEntrada;
    }

    public int getValorSalida() {
        return this.valorSalida;
    }

    public void setValorSalida(int valorSalida) {
        this.valorSalida = valorSalida;
    }

    // METODOS

}
