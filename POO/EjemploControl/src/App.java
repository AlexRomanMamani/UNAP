public class App {
    public static void main(String[] args) throws Exception {
        // INICIO DE SECCION PARA PROGRAMAR
        // SENTENCIA DE CONTROL

        int i;
        for (i = 0; i < 10; i++) {
            System.out.println(i);
        }

        i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
    }
}
